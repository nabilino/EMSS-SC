function aboutInit()
{
	if (wndArguments == null || typeof(wndArguments) == "undefined")
	{
		// can't use messageBox here: it will also fail
		alert(msgDOMAIN);
		window.close();
		return;
	}
  	txtVersion.innerText = "Version " + wndArguments[0].designStudio.activeDesigner.versionNumber;
	document.body.style.visibility="visible"
	btnOK.focus()
}

//-----------------------------------------------------------------------------
function aboutOnKeyDown()
{
	if (event.keyCode==13 || event.keyCode==27)	// enter, ESC
	{
		document.all.btnOK.click()
		event.cancelBubble=true
		event.returnValue=false
		return true
	}
	return(false)
}
